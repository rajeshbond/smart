package store

import (
	"context"
	"database/sql"
)

//------------------------------------------------------------------------------
// Store
//------------------------------------------------------------------------------

type Store struct {
	db *sql.DB
}

//------------------------------------------------------------------------------
// Constructor
//------------------------------------------------------------------------------

func NewStore(db *sql.DB) *Store {
	return &Store{
		db: db,
	}
}

//------------------------------------------------------------------------------
// DB
//------------------------------------------------------------------------------

// func (s *Store) DB() *sql.DB {
