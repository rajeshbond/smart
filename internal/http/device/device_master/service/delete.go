package service

import (
	"context"
)

//------------------------------------------------------------------------------
// Delete
//------------------------------------------------------------------------------

func (s *Service) Delete(
	ctx context.Context,
	id int64,
	userID int64,
) error {

	//----------------------------------------------------------------------
	// Begin Transaction
	//----------------------------------------------------------------------

	tx, err := s.Store.BeginTx(ctx)
	if err != nil {
		return err
	}

	defer func() {
		if err != nil {
			_ = tx.Rollback()
		}
	}()

	//----------------------------------------------------------------------
	// Delete Device
	//----------------------------------------------------------------------

	err = s.Store.DeleteTx(
		ctx,
		tx,
		id,
		userID,
	)
	if err != nil {
		return err
	}

	//----------------------------------------------------------------------
	// Commit Transaction
	//----------------------------------------------------------------------

	err = tx.Commit()
	if err != nil {
		return err
	}

	return nil
}
